@router.post('/delete')
def Leave_Type(request: Request, db: Session = Depends(get_db), delete_id: int = Form(...)):
    try:
        if 'loginer_details' in request.session:
            token = request.session['loginer_details']
            try:
                payload = jwt.decode(token, BaseConfig.SECRET_KEY, algorithms=[BaseConfig.ALGORITHM])
                loginer_id : int= payload.get("empid") 
                try:
                    if loginer_id:
                        emp_data = db.query(models.Employee).filter(models.Employee.id==loginer_id).filter(models.Employee.status=='ACTIVE').first()
                        if emp_data.Lock_screen == 'OFF':
                            # Delete the specified Leave_Type record
                            leave_type = db.query(models.Leave_Type).filter(models.Leave_Type.id == delete_id).first()
                            if leave_type:
                                db.delete(leave_type)
                                db.commit()
                                remaining_leave_types = db.query(models.Leave_Type).order_by(models.Leave_Type.id).all()
                                # Update the IDs of the remaining records
                                for i, leave_type in enumerate(remaining_leave_types, start=1):
                                    db.execute(update(models.Leave_Type).where(models.Leave_Type.id == leave_type.id).values(id=i))
                                db.commit()
                                # Redirect to the home page with the updated list
                                return RedirectResponse('/HrmTool/Administration/leave_type', status_code=303)
                        else:
                            return RedirectResponse('/HrmTool/Lock/lockscreen',status_code=302)
                    else:
                        return RedirectResponse('/HrmTool/login/login',status_code=302)
                except:
                    return RedirectResponse('/HrmTool/login/login',status_code=302)
            except JWTError:
                return RedirectResponse('/HrmTool/login/login',status_code=302)
        else:
            return RedirectResponse('/HrmTool/login/login',status_code=303)
    except JWTError:
            return RedirectResponse('/HrmTool/login/login',status_code=302)
    except Exception as e:
        return JSONResponse(status_code=status.HTTP_500_INTERNAL_SERVER_ERROR, content={"detail": "Internal Server Error"}) 
       
